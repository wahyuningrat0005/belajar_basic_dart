VARIABLE
- Variable merupakan tempat untuk menyimpan data,ada
banyak sekali tipe data di dart
- Variable wajib memiliki tipe data dan nama variable
ketika kita memanggil variable tersebut kita cukup
menyebutkan nama variablenya
CARA MEMBUAT VARIABLE
- untuk membuat deklarasi variable kita bisa
gunakan TipeData namaVarible
- biasanya penamaan variable di dart menggunakan
camelCase
-kata kunci yang digunakan "var"
FINAL
- secara default, variable di dart bisa dideklarasikan ulang
- untuk melakukan ini kita bisa gunakan kata kunci
"final"
CONST
- kata kunci final digunakan agar variable tidak bisa
dideklarasikan ulang,namun nilai dari variable
sendiri bisa dirubah
- didart terdapat kata kunci constanta digunkan
untuk menjadikan varible dan nilainya menjadi immutable atau tidak bisa dirubah sama sekali

