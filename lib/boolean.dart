/*
tipe data boolean
boolean adalah tipe data yang hanya memilki dua nilai
yaitu salah dan benar
boolean diresepsantikan dengan kata kunci bool
untuk nilai benar menggunakan kata kunci true
untuk nilai salah menggunakan kata kunci false
*/
void main() {
  bool mulai = false;
  print(mulai);
  mulai = true;
  print(mulai);
}
