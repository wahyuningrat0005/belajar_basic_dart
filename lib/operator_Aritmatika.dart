/*
(+) penambahan
(-) pengurangan
(*) perkalian
(/) pembagian hasil double
(~/) pembagian hasil integer
(%) Sisa Bagi
*/
void main() {
  var result1 = 10 + 10;
  var result2 = 100 - 10;
  var result3 = 10 / 3;
  var result4 = 10 * 10;
  var result5 = 10 ~/ 3;
  var result6 = 10 % 3;

  print(result1);
  print(result2);
  print(result3);
  print(result4);
  print(result5);
  print(result6);
}
